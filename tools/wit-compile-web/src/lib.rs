#[cfg(test)]
mod tests {
    use super::*;

    const MINI_WIT: &str = r#"
        package test:mini@0.1.0;

        interface types {
            record sample {
                field-a: string,
                count: u32,
                %type: actr-type,
            }

            record actr-type {
                name: string,
            }

            variant colour {
                red,
                green(string),
            }

            enum delivery-state {
                not-sent,
                delivery-uncertain,
            }
        }

        interface host {
            use types.{sample, actr-type};
            ping: func(arg: sample) -> result<u32, string>;
            log-message: func(msg: string);
        }

        interface workload {
            use types.{sample};
            dispatch: func(arg: sample) -> result<u32, string>;
            on-start: func();
        }

        world w {
            import host;
            export workload;
        }
    "#;

    fn load_inmem(source: &str) -> WitModel {
        let mut resolve = Resolve::new();
        resolve.push_str("in-memory.wit", source).expect("parse");
        flatten(&resolve).expect("flatten")
    }

    #[test]
    fn records_and_variants_surface() {
        let model = load_inmem(MINI_WIT);
        assert!(model.records.contains_key("sample"));
        assert!(model.enums.contains_key("delivery-state"));
        assert!(model.variants.contains_key("colour"));
    }

    #[test]
    fn types_rs_contains_escaped_keyword_rename() {
        let model = load_inmem(MINI_WIT);
        let out = emit_types(&model);
        // The WIT field `%type` on `sample` should become
        // `actr_type` in Rust, with a serde rename back to the WIT
        // wire form (`type`, no leading %).
        assert!(
            out.contains("pub actr_type: ActrType"),
            "output was:\n{out}"
        );
        assert!(out.contains("#[serde(rename = \"type\")]"));
    }

    #[test]
    fn guest_rs_has_async_wrappers() {
        let model = load_inmem(MINI_WIT);
        let out = emit_guest(&model);
        assert!(out.contains("pub async fn ping_with_request_id"));
        assert!(out.contains("pub async fn log_message_with_request_id"));
        assert!(out.contains("JsFuture::from"));
    }

    #[test]
    fn guest_rs_threads_request_id_through() {
        let model = load_inmem(MINI_WIT);
        let out = emit_guest(&model);
        // Wrapper takes `request_id: &str` as the leading parameter.
        assert!(
            out.contains("pub async fn ping_with_request_id(request_id: &str"),
            "output was:\n{out}"
        );
        // Extern declaration takes `request_id: JsValue` first so the
        // SW host can dispatch by request_id.
        assert!(
            out.contains("fn __actr_host_ping(request_id: JsValue"),
            "output was:\n{out}"
        );
        // Body converts it to a JsString before the raw call.
        assert!(out.contains("JsValue::from_str(request_id)"));
        // Call threads the request_id JS value as the first argument.
        assert!(out.contains("__actr_host_ping(__js_request_id"));
    }

    #[test]
    fn host_rs_has_crate_private_trait_and_register() {
        let model = load_inmem(MINI_WIT);
        let out = emit_host(&model);
        // Trait is `#[doc(hidden)] pub` so it can bound the public
        // `register_workload` fn without triggering `private_bounds`.
        assert!(out.contains("#[doc(hidden)]\npub trait Workload"));
        assert!(
            !out.contains("pub(crate) trait Workload"),
            "Workload trait should no longer be pub(crate)"
        );
        assert!(out.contains("pub fn register_workload"));
        assert!(out.contains("W: Workload + Clone + 'static"));
        assert!(out.contains("async fn dispatch"));
        assert!(out.contains("async fn on_start"));
        assert!(out.contains("#[wasm_bindgen(js_name = \"dispatch\")"));
    }
}
