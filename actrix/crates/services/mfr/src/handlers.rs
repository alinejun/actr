use crate::{
    MfrError, MfrManager,
    manager::{NonceRequest, PublishRequest},
    model::MfrStatus,
};
use axum::{
    Json, Router,
    extract::{Path, Query, State},
    routing::{delete, get, post},
};
use serde::{Deserialize, Serialize};
use std::sync::Arc;

#[derive(Clone)]
pub struct MfrState {
    pub manager: Arc<MfrManager>,
}

/// Public routes — only endpoints that external systems (Hyper runtime, CLI) truly need.
/// These are mounted at `/mfr` without authentication.
pub fn create_public_router(state: MfrState) -> Router {
    Router::new()
        // Hyper runtime: fetches MFR public key for package signature verification
        .route("/{name}/verifying_key", get(get_verifying_key))
        // CLI `actr pkg publish`: request a one-time nonce for publish auth
        .route("/pkg/nonce", post(request_nonce))
        // CLI `actr pkg publish`: publishes signed packages (authenticated via nonce challenge-response)
        .route("/pkg/publish", post(publish_package))
        .with_state(state)
}

/// Admin routes — all management and state-changing operations.
/// These are mounted at `/admin/api/mfr` behind JWT authentication middleware.
pub fn create_admin_router(state: MfrState) -> Router {
    Router::new()
        // Manufacturer registration & verification flow
        .route("/apply", post(apply))
        .route("/{id}/verify", post(verify_github))
        .route("/{id}/challenge", get(get_challenge))
        .route("/{id}/status", get(get_status))
        .route("/resolve/{name}", get(resolve_by_name))
        // Package management
        .route("/pkg", get(list_packages))
        .route("/pkg/{id}/revoke", post(revoke_package))
        // Admin privileged operations
        .route("/admin/list", get(admin_list))
        .route("/admin/{id}/approve", post(admin_approve))
        .route("/admin/{id}/suspend", post(admin_suspend))
        .route("/admin/{id}/reinstate", post(admin_reinstate))
        .route("/admin/{id}/renew", post(admin_renew))
        .route("/admin/{id}/keys", get(admin_list_keys))
        .route(
            "/admin/keys/{history_id}/revoke",
            post(admin_revoke_historical_key),
        )
        .route("/admin/{id}", delete(admin_delete))
        .with_state(state)
}

// --- Request / Response types ---

#[derive(Deserialize)]
struct ApplyRequest {
    github_login: String,
    contact: Option<String>,
}

#[derive(Serialize)]
struct ApplyResponse {
    mfr_id: i64,
    challenge_token: String,
    expires_at: i64,
    verify_file: String,
    instructions: String,
}

#[derive(Deserialize)]
struct ListPackagesQuery {
    mfr: Option<String>,
}

#[derive(Deserialize, Default)]
struct VerifyRequest {
    /// Optional user-provided Ed25519 public key (base64).
    /// If omitted, a new keypair is generated by the server.
    pub public_key: Option<String>,
}

#[derive(Deserialize, Default)]
struct AdminApproveRequest {
    /// Optional user-provided Ed25519 public key (base64).
    /// If omitted, a new keypair is generated by the server.
    pub public_key: Option<String>,
}

#[derive(Deserialize, Default)]
struct AdminRenewRequest {
    /// Optional user-provided Ed25519 public key (base64).
    /// If omitted, a new keypair is generated by the server.
    pub public_key: Option<String>,
}

#[derive(Deserialize)]
struct GetVerifyingKeyQuery {
    pub key_id: Option<String>,
}

// --- Helpers ---

/// Parse an optional JSON body: empty bytes → Ok(None), non-empty → deserialize.
/// This handles the case where browsers send `Content-Type: application/json` with an empty body.
fn parse_optional_body<T: serde::de::DeserializeOwned>(
    body: &axum::body::Bytes,
) -> Result<Option<T>, MfrError> {
    if body.is_empty() {
        return Ok(None);
    }
    serde_json::from_slice(body)
        .map(Some)
        .map_err(|e| MfrError::InvalidName(format!("invalid request body: {e}")))
}

// --- Handlers ---

async fn apply(
    State(s): State<MfrState>,
    Json(req): Json<ApplyRequest>,
) -> Result<Json<ApplyResponse>, MfrError> {
    let (mfr, challenge) = s
        .manager
        .apply(&req.github_login, req.contact.as_deref())
        .await?;
    let filename = crate::github::verify_filename(s.manager.domain());
    Ok(Json(ApplyResponse {
        mfr_id: mfr.id,
        challenge_token: challenge.token.clone(),
        expires_at: challenge.expires_at,
        verify_file: filename.clone(),
        instructions: format!(
            "Create a public GitHub repo '{}/{}' with a file '{}' containing the token above, then call POST /{}/verify",
            mfr.name,
            crate::github::VERIFY_REPO,
            filename,
            mfr.id,
        ),
    }))
}

async fn verify_github(
    State(s): State<MfrState>,
    Path(id): Path<i64>,
    body: axum::body::Bytes,
) -> Result<Json<crate::manager::ActivateResponse>, MfrError> {
    let public_key = parse_optional_body::<VerifyRequest>(&body)?.and_then(|r| r.public_key);
    let response = s.manager.verify_github(id, public_key.as_deref()).await?;
    Ok(Json(response))
}

async fn get_challenge(
    State(s): State<MfrState>,
    Path(id): Path<i64>,
) -> Result<Json<ApplyResponse>, MfrError> {
    let mfr = s.manager.get_status(id).await?;
    let challenge = s.manager.get_challenge(id).await?;
    let filename = crate::github::verify_filename(s.manager.domain());
    Ok(Json(ApplyResponse {
        mfr_id: mfr.id,
        challenge_token: challenge.token,
        expires_at: challenge.expires_at,
        verify_file: filename.clone(),
        instructions: format!(
            "Create a public GitHub repo '{}/{}' with a file '{}' containing the token above, then call POST /{}/verify",
            mfr.name,
            crate::github::VERIFY_REPO,
            filename,
            mfr.id,
        ),
    }))
}

