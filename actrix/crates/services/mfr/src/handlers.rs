async fn resolve_by_name(
    State(s): State<MfrState>,
    Path(name): Path<String>,
) -> Result<Json<crate::manager::MfrPublicInfo>, MfrError> {
    Ok(Json(s.manager.resolve_by_name(&name).await?))
}

async fn get_verifying_key(
    State(s): State<MfrState>,
    Path(name): Path<String>,
    Query(q): Query<GetVerifyingKeyQuery>,
) -> Result<Json<serde_json::Value>, MfrError> {
    let info = if let Some(key_id) = q.key_id {
        s.manager.resolve_key_by_id(&name, &key_id).await?
    } else {
        s.manager.resolve_by_name(&name).await?
    };
    Ok(Json(serde_json::json!({
        "public_key": info.public_key,
        "key_id": info.key_id,
    })))
}

/// POST /mfr/pkg/nonce — request a one-time nonce for publish authentication.
async fn request_nonce(
    State(s): State<MfrState>,
    Json(req): Json<NonceRequest>,
) -> Result<Json<serde_json::Value>, MfrError> {
    use base64::Engine as _;
    let nonce_bytes = s.manager.request_nonce(&req.manufacturer).await?;
    let nonce_b64 = base64::engine::general_purpose::STANDARD.encode(&nonce_bytes);
    Ok(Json(serde_json::json!({ "nonce": nonce_b64 })))
}

