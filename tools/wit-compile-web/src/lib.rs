/// Generate all three Rust sources from a WIT file on disk. Used by
/// both the binary's write path and the `--check` path so the bytes
/// being diffed are the exact bytes the write path would produce.
pub fn generate_from_path(wit_path: &Path) -> Result<Generated> {
    let model = load_model(wit_path)?;
    Ok(Generated {
        types: format_rust_source("types.rs", emit_types(&model))?,
        guest: format_rust_source("guest.rs", emit_guest(&model))?,
        host: format_rust_source("host.rs", emit_host(&model))?,
    })
}

fn format_rust_source(file: &'static str, source: String) -> Result<String> {
    let mut child = Command::new("rustfmt")
        .args(["--emit", "stdout"])
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .with_context(|| "spawn rustfmt for generated source")?;

    child
        .stdin
        .as_mut()
        .expect("rustfmt stdin is piped")
        .write_all(source.as_bytes())
        .with_context(|| format!("write generated {file} to rustfmt"))?;

    let output = child
        .wait_with_output()
        .with_context(|| format!("wait for rustfmt on generated {file}"))?;
    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        bail!("rustfmt failed for generated {file}: {stderr}");
    }

    String::from_utf8(output.stdout)
        .with_context(|| format!("rustfmt output for {file} was not UTF-8"))
}

/// Write the three sources under `out_dir`, creating the directory if
/// it doesn't exist.
pub fn write_outputs(out_dir: &Path, out_gen: &Generated) -> Result<()> {
    std::fs::create_dir_all(out_dir).with_context(|| format!("create {}", out_dir.display()))?;
    std::fs::write(out_dir.join("types.rs"), &out_gen.types)
        .with_context(|| format!("write {}/types.rs", out_dir.display()))?;
    std::fs::write(out_dir.join("guest.rs"), &out_gen.guest)
        .with_context(|| format!("write {}/guest.rs", out_dir.display()))?;
    std::fs::write(out_dir.join("host.rs"), &out_gen.host)
        .with_context(|| format!("write {}/host.rs", out_dir.display()))?;
    Ok(())
}

/// Drift report produced by [`check_outputs`]. An empty `diffs` vec
/// means the checked-in sources are up to date.
#[derive(Debug, Default)]
pub struct CheckReport {
    pub diffs: Vec<DriftEntry>,
}

#[derive(Debug)]
pub struct DriftEntry {
    pub file: &'static str,
    pub reason: &'static str,
}

/// Compare the generated output to the on-disk sources without
/// writing. Returns a [`CheckReport`]; an empty `diffs` vec means the
/// caller can exit 0.
pub fn check_outputs(out_dir: &Path, out_gen: &Generated) -> Result<CheckReport> {
    let mut report = CheckReport::default();
    for (file, expected) in [
        ("types.rs", &out_gen.types),
        ("guest.rs", &out_gen.guest),
        ("host.rs", &out_gen.host),
    ] {
        let path = out_dir.join(file);
        let on_disk = match std::fs::read_to_string(&path) {
            Ok(s) => s,
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => {
                report.diffs.push(DriftEntry {
                    file: static_str(file),
                    reason: "missing on disk",
                });
                continue;
            }
            Err(e) => return Err(anyhow!("read {}: {e}", path.display())),
        };
        if on_disk != *expected {
            report.diffs.push(DriftEntry {
                file: static_str(file),
                reason: "content differs from generator output",
            });
        }
    }
    Ok(report)
}

/// Interns a `&str` to `&'static str` by matching the three known
/// filenames. Keeps [`DriftEntry`] owning `'static` borrows for cheap
/// formatting without dragging in a `'static` string dep.
fn static_str(s: &str) -> &'static str {
    match s {
        "types.rs" => "types.rs",
        "guest.rs" => "guest.rs",
        "host.rs" => "host.rs",
        other => Box::leak(other.to_string().into_boxed_str()),
    }
}

